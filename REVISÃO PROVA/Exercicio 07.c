/*Crie um programa que calcule o volume de uma esfera. Solicite o raio (r) ao usuário e exiba o resultado. Use a fórmula:*/

#include <stdlib.h>
#include <stdio.h>
#include <locale.h>
#include <math.h>
#include <time.h>

int main (){
    setlocale(LC_ALL, "Portuguese");
    float r, V;
    printf("Digite o raio da esfera: ");
    scanf("%f", &r);
    V = 4.0/3.0*(M_PI*pow(r,3));
    printf("O volume da esfera é: %.2f", V);
    return 0;
}